/**
 * Modèle de données du "Guide des fonctionnalités" (ex cahier de recette).
 *
 * Le document est entièrement piloté par le tableau `CAHIER_RECETTE` ci-dessous : ajouter un
 * module, une fonctionnalité (menu) ou un scénario ne demande aucune modification du composant
 * ni du template — c'est ce qui rend le document "évolutif". La hiérarchie affichée dans le menu
 * de gauche reprend directement `module -> fonctionnalite (menu) -> scénarios (sous-menu)`.
 *
 * L'inventaire ne se limite pas aux routes de premier niveau : plusieurs écrans regroupent
 * plusieurs fonctionnalités distinctes derrière des onglets (ex. `sales-management-home`,
 * `facturation-layout`, `finances-layout`, `comptabilite-layout`, `inventory-home`) ou des vues
 * maître-détail (ex. fiche produit, fiche client, fiche facture). Chaque onglet/panneau
 * significatif est listé comme une fonctionnalité à part pour ne pas les faire disparaître
 * derrière le seul écran conteneur.
 *
 * Ce document est orienté "mode d'emploi" : chaque scénario explique le besoin métier auquel il
 * répond, comment la fonctionnalité fonctionne concrètement, les étapes à suivre et le résultat
 * attendu — il ne porte plus de suivi de recette (pas de statut ni de commentaire de test).
 */

export interface ScenarioRecette {
  /** Référence stable, ex. "VTE-01". */
  id: string;
  titre: string;
  /** Le besoin métier / problème auquel cette fonctionnalité répond. */
  besoin: string;
  /** Explication de fonctionnement : logique, règles, impacts (stock, caisse, comptabilité...). */
  fonctionnement: string;
  prerequis?: string;
  etapes: string[];
  resultatAttendu: string;
  /** Masque ce scénario de l'affichage sans le supprimer du document. Défaut : false. */
  hidden?: boolean;
}

export interface FonctionnaliteRecette {
  nom: string;
  description?: string;
  scenarios: ScenarioRecette[];
  /** Masque cette fonctionnalité (et tous ses scénarios) de l'affichage. Défaut : false. */
  hidden?: boolean;
  /**
   * Version du projet dans laquelle cette fonctionnalité a été livrée, ex. `"1.7.8"` —
   * alignée sur les migrations Flyway `V{version}__...sql` qui l'ont introduite.
   * Absente pour les fonctionnalités historiques, déjà présentes avant la mise en place
   * de ce suivi : ce n'est pas une garantie d'ancienneté, seulement l'absence
   * d'information.
   *
   * Permet de distinguer, dans l'affichage :
   * - une fonctionnalité **récente** : `version` renseignée, proche de la version
   *   courante de l'application ;
   * - une fonctionnalité **roadmap** : `roadmap: true`, pas encore livrée — `version`
   *   indique alors, si elle est connue, la version cible de livraison.
   */
  version?: string;
  /**
   * Fonctionnalité planifiée mais pas encore disponible dans l'application — à afficher
   * dans une section « Roadmap » distincte plutôt que comme fonctionnalité utilisable.
   * Défaut : false.
   */
  roadmap?: boolean;
}

export interface ModuleRecette {
  id: string;
  nom: string;
  icone: string;
  description: string;
  fonctionnalites: FonctionnaliteRecette[];
}

