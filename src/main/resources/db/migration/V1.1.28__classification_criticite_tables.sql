DROP TABLE IF EXISTS v_produit_metriques_classification;
-- Vue analytique des métriques de classification par produit
-- Note: Filtre sur magasin_id = 1 (magasin par défaut) pour le calcul du stock
CREATE OR REPLACE VIEW v_produit_metriques_classification AS
SELECT
    p.id AS produit_id,
    p.libelle,
    p.classe_criticite AS classe_actuelle,

    -- VMM sur 12 mois
    COALESCE(
        (SELECT AVG(quantite_vendue)
         FROM ventes_mensuelles_agregees vma
         WHERE vma.produit_id = p.id
           AND vma.annee_mois >= TO_CHAR(NOW() - INTERVAL '12 months', 'YYYY-MM')
           AND vma.is_frozen = TRUE
        ), 0
    )::INTEGER AS vmm_12_mois,

    -- CA total sur 12 mois (en centimes)
    COALESCE(
        (SELECT SUM(montant_ca)
         FROM ventes_mensuelles_agregees vma
         WHERE vma.produit_id = p.id
           AND vma.annee_mois >= TO_CHAR(NOW() - INTERVAL '12 months', 'YYYY-MM')
           AND vma.is_frozen = TRUE
        ), 0
    )::BIGINT AS ca_12_mois,

    -- Quantité vendue totale sur 12 mois
    COALESCE(
        (SELECT SUM(quantite_vendue)
         FROM ventes_mensuelles_agregees vma
         WHERE vma.produit_id = p.id
           AND vma.annee_mois >= TO_CHAR(NOW() - INTERVAL '12 months', 'YYYY-MM')
           AND vma.is_frozen = TRUE
        ), 0
    )::INTEGER AS qte_vendue_12_mois,

    -- Fréquence de vente (nb de mois avec ventes)
    COALESCE(
        (SELECT COUNT(*)
         FROM ventes_mensuelles_agregees vma
         WHERE vma.produit_id = p.id
           AND vma.annee_mois >= TO_CHAR(NOW() - INTERVAL '12 months', 'YYYY-MM')
           AND vma.is_frozen = TRUE
           AND vma.quantite_vendue > 0
        ), 0
    )::INTEGER AS frequence_vente_mois,

    -- Stock actuel (filtré sur magasin par défaut id=1)
    COALESCE(
        (SELECT SUM(sp.qty_stock + sp.qty_ug)
         FROM stock_produit sp
         JOIN storage s ON sp.storage_id = s.id
         WHERE sp.produit_id = p.id
           AND s.magasin_id = 1
        ), 0
    )::INTEGER AS stock_actuel,

    -- Rotation annuelle = Ventes annuelles / Stock moyen
    CASE
        WHEN COALESCE(
            (SELECT SUM(sp.qty_stock + sp.qty_ug)
             FROM stock_produit sp
             JOIN storage s ON sp.storage_id = s.id
             WHERE sp.produit_id = p.id
               AND s.magasin_id = 1
            ), 0) > 0
        THEN
            COALESCE(
                (SELECT SUM(quantite_vendue)
                 FROM ventes_mensuelles_agregees vma
                 WHERE vma.produit_id = p.id
                   AND vma.annee_mois >= TO_CHAR(NOW() - INTERVAL '12 months', 'YYYY-MM')
                   AND vma.is_frozen = TRUE
                ), 0
            )::DECIMAL / COALESCE(
                (SELECT SUM(sp.qty_stock + sp.qty_ug)
                 FROM stock_produit sp
                 JOIN storage s ON sp.storage_id = s.id
                 WHERE sp.produit_id = p.id
                   AND s.magasin_id = 1
                ), 1)
        ELSE 0
    END AS rotation_annuelle,

    -- Ancienneté du produit (en mois)
    (EXTRACT(YEAR FROM AGE(NOW(), p.created_at)) * 12 +
     EXTRACT(MONTH FROM AGE(NOW(), p.created_at)))::INTEGER AS anciennete_mois,

    -- Flag nouveau produit (basé sur config par défaut 6 mois)
    CASE
        WHEN EXTRACT(YEAR FROM AGE(NOW(), p.created_at)) * 12 +
             EXTRACT(MONTH FROM AGE(NOW(), p.created_at)) < 6
        THEN TRUE
        ELSE FALSE
    END AS est_nouveau_produit,

    p.created_at AS date_creation_produit

FROM produit p
WHERE p.status = 'ENABLE'
  AND p.type_produit != 'DETAIL';

COMMENT ON VIEW v_produit_metriques_classification IS 'Vue des métriques de classification pour chaque produit actif (stock filtré sur magasin par défaut id=1)';

