
create table classification_config
(
  id                          integer generated by default as identity
    primary key,
  auto_classification_enabled boolean       not null default true,
  changement_min_score        integer       not null default 10
    constraint classification_config_changement_min_score_check
      check ((changement_min_score <= 50) AND (changement_min_score >= 0)),
  nb_mois_analyse             integer       not null default 12
    constraint classification_config_nb_mois_analyse_check
      check ((nb_mois_analyse >= 6) AND (nb_mois_analyse <= 24)),
  nb_mois_min_nouveau_produit integer       not null default 6
    constraint classification_config_nb_mois_min_nouveau_produit_check
      check ((nb_mois_min_nouveau_produit >= 1) AND (nb_mois_min_nouveau_produit <= 12)),
  poids_ca                    numeric(3, 2) not null default 0.50,
  poids_frequence             numeric(3, 2) not null default 0.20,
  poids_rotation              numeric(3, 2) not null default  0.30,
  rotation_a                  numeric(5, 2) not null default 8.0,
  rotation_a_plus             numeric(5, 2) not null default 12.0,
  rotation_b                  numeric(5, 2) not null default 4.0,
  rotation_c                  numeric(5, 2) not null default 2.0,
  seuil_a                     integer       not null DEFAULT 80
    constraint classification_config_seuil_a_check
      check ((seuil_a <= 100) AND (seuil_a >= 0)),
  seuil_a_plus                integer       not null default 90
    constraint classification_config_seuil_a_plus_check
      check ((seuil_a_plus <= 100) AND (seuil_a_plus >= 0)),
  seuil_b                     integer       not null default 60
    constraint classification_config_seuil_b_check
      check ((seuil_b <= 100) AND (seuil_b >= 0)),
  seuil_c                     integer       not null default 40
    constraint classification_config_seuil_c_check
      check ((seuil_c <= 100) AND (seuil_c >= 0)),
  updated_at                  timestamp(6)  not null default now(),
  updated_by                  integer
    constraint fkogdh3vnqy20an3yowt4fep8ju
      references app_user
);



