CREATE TABLE pharmaml_envoi
(
  id                 integer generated by default as identity primary key,
  commande_id        INTEGER     NOT NULL,
  commande_date      DATE        NOT NULL,
  fournisseur_id     INTEGER     NOT NULL,
  statut             VARCHAR(20) NOT NULL DEFAULT 'PENDING',
  ref_message        VARCHAR(30),
  tentatives         INTEGER     NOT NULL DEFAULT 0,
  derniere_tentative TIMESTAMP,
  xml_requete_path   VARCHAR(255),
  xml_reponse_path   VARCHAR(255),
  total_lignes       INTEGER,
  lignes_acceptees   INTEGER,
  lignes_rupture     INTEGER,
  created_at         TIMESTAMP   NOT NULL DEFAULT NOW(),
  CONSTRAINT fk_pharmaml_commande
    FOREIGN KEY (commande_id, commande_date)
      REFERENCES commande (id, order_date)
);

CREATE INDEX idx_pharmaml_envoi_commande ON pharmaml_envoi (commande_id, commande_date);
CREATE INDEX idx_pharmaml_envoi_statut ON pharmaml_envoi (statut);


CREATE TABLE substitution_proposee
(
  id                integer generated by default as identity primary key,
  commande_id       INTEGER     NOT NULL,
  commande_date     DATE        NOT NULL,
  commande_order_date     DATE        NOT NULL,
  order_line_id     INTEGER     NOT NULL,
  fournisseur_id    INTEGER     NOT NULL,
  cip_propose       VARCHAR(13) NOT NULL,
  designation       VARCHAR(255),
  type_codification VARCHAR(10),
  quantite          INTEGER     NOT NULL,
  statut            VARCHAR(20) NOT NULL DEFAULT 'EN_ATTENTE',
  created_at        TIMESTAMP   NOT NULL DEFAULT NOW(),
  CONSTRAINT fk_substitution_commande
    FOREIGN KEY (commande_id, commande_order_date)
      REFERENCES commande (id, order_date),
  CONSTRAINT fk_substitution_order_line
    FOREIGN KEY (order_line_id, commande_date)
      REFERENCES order_line (id, order_date)
);

CREATE INDEX idx_substitution_commande ON substitution_proposee (commande_id, commande_order_date);
CREATE INDEX idx_substitution_statut ON substitution_proposee (statut);


