-- Trigger pour mettre à jour updated_at sur classification_config
CREATE OR REPLACE FUNCTION update_classification_config_updated_at()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = NOW();
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trigger_classification_config_updated_at
    BEFORE UPDATE ON classification_config
    FOR EACH ROW
    EXECUTE FUNCTION update_classification_config_updated_at();


